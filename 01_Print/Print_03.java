//화면에 연속표시
public class Print_03 {
    public static void main(String[] args) {
        System.out.println("첫 Java 프로그램 입니다. 화면에 출력하고 있습니다."); //println 은 줄바꿈 O

        System.out.print("첫 Java 프로그램 입니다."); //print는 줄바꿈 x
        System.out.println(" 화면에 출력하고 있습니다.");

        System.out.println("첫 Java 프로그램 입니다."+" 화면에 출력하고 있습니다.");
    }
}