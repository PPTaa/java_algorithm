//문자열 초기화와 대입
public class Variable_16 {
    public static void main(String[] args) {
        String s1 = "ABC";
        String s2 = "XYZ";

        System.out.println("s1 :" + s1);
        System.out.println("s2 :" + s2);
        
        s1 = "가나다";
        s2 = "타파하";
        System.out.println("s1 :" + s1);
        System.out.println("s2 :" + s2);
    
    }
}