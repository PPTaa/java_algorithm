//정수 82 와 17의 합과 차를 구하는 프로그램을 다양한 방법으로 작성
public class Variable_01 {
    public static void main(String[] args) {
        int a = 82;
        int b = 17;
        System.out.println("a + b = " + (a+b));
        System.out.println("a - b = " + (a-b));

        System.out.println(82 + 17);
        System.out.println(82 - 17);
    }
}